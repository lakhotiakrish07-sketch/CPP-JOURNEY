#include <iostream>
using namespace std ;
int main (){
    float x, y;
    cin>>x>>y;
    cout<<"addition:"<<x+y<<endl;
    cout<<"sub:"<<x-y<<endl;
    cout<<"multiplication:"<<x*y<<endl;
    cout<<"division:"<<x/y<<endl;

    //incrementation and decrementation 
    cout<<++x<<endl;//first increase then print
    cout<<x++<<endl;//first print then inc
    cout<<--x<<endl;//first decrease then print
    cout<<x--<<endl;//first print then dec

    /*this is
    a multi line comment 
    */

    return 0 ;


}
