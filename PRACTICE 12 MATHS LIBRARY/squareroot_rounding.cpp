#include <iostream>
#include <cmath>
using namespace std;

int main() {
    double num;
    cin >> num;

    cout << "Square root: " << sqrt(num) << endl;
    cout << "Floor: " << floor(num) << endl;
    cout << "Ceil: " << ceil(num) << endl;
    cout << "Round: " << round(num) << endl;

    return 0;
}